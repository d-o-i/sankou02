//=============================================================================
// コンストラクタ
//=============================================================================
JumpState::JumpState(Player *owner) : owner_(owner)
{
}

//=============================================================================
// デストラクタ
//=============================================================================
JumpState::~JumpState()
{
	owner_ = nullptr;
}

//=============================================================================
// 更新
//=============================================================================
void JumpState::Update(int AnimCurtID)
{
	// 状態変化
	// 地面に接触したらダッシュに移行
	if (owner_->GetHitGround() || owner_->GetHitPaint())
	{
		owner_->ChangeAnim(Running);
		owner_->ChangeState(new RunningState(owner_));
		return;
	}
}