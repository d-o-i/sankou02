#include "Main.h"
#include "SpInkState.h"

//*****************************************************************************
// マクロ定義
//*****************************************************************************
#define ACTIVE_TIME	(600)

//=============================================================================
// コンストラクタ
//=============================================================================
SpInkState::SpInkState(Item *owner) : owner_(owner)
{
	cnt = 0;
}

//=============================================================================
// デストラクタ
//=============================================================================
SpInkState::~SpInkState()
{
	owner_ = nullptr;
}

//=============================================================================
// 更新
//=============================================================================
void SpInkState::Update()
{
	cnt++;

	// アイテムの効果が終了
	if (cnt == ACTIVE_TIME)
	{
		owner_->Reset();
		owner_->GetPlayer()->GetPaintManager()->SetSpInk(false);
	}

	// プレイヤーにエフェクト発生
	owner_->GetPlayer()->PowwrUpEffect();

}

//=============================================================================
// 有効にするときだけ使う
//=============================================================================
void SpInkState::Start()
{
	cnt = 0;
	owner_->GetPlayer()->GetPaintManager()->SetSpInk(true);
}